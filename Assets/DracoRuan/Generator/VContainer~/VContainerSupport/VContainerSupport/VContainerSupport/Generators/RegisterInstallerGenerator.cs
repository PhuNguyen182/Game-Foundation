using System.Linq;
using System.Text;
using System.Collections.Generic;
using System.Collections.Immutable;
using Microsoft.CodeAnalysis;
using Microsoft.CodeAnalysis.CSharp.Syntax;
using Microsoft.CodeAnalysis.Text;
using VContainerSupport.Models;

namespace VContainerSupport.Generators;

[Generator]
public class RegisterInstallerGenerator : IIncrementalGenerator
{
    private const string AutoRegisterInstallerAttributeName = "RegisterInstallerAttribute";

    private static readonly StringBuilder InstallerManagerStringBuilder = new();
    private static readonly StringBuilder InstallerNamespaceStringBuilder = new();
    private static readonly StringBuilder InstallerTaskStringBuilder = new();

    public void Initialize(IncrementalGeneratorInitializationContext context)
    {
        IncrementalValuesProvider<InstallerRegistrationModel> classDeclarations = context.SyntaxProvider
            .CreateSyntaxProvider(
                predicate: (syntaxNode, _) => syntaxNode is ClassDeclarationSyntax { AttributeLists.Count: > 0 },
                transform: (syntaxContext, _) => GetRegistrationData(syntaxContext))
            .Where(registrationModel => registrationModel is not null);
        
        IncrementalValueProvider<ImmutableArray<InstallerRegistrationModel>> registrationData = classDeclarations.Collect();
        context.RegisterSourceOutput(registrationData, Execute);
    }

    private static InstallerRegistrationModel GetRegistrationData(GeneratorSyntaxContext context)
    {
        ClassDeclarationSyntax classDeclarationSyntax = (ClassDeclarationSyntax)context.Node;
        INamedTypeSymbol symbol = context.SemanticModel.GetDeclaredSymbol(classDeclarationSyntax) as INamedTypeSymbol;
        AttributeData attributeData = symbol?.GetAttributes().FirstOrDefault(attribute =>
            attribute.AttributeClass?.Name is AutoRegisterInstallerAttributeName);

        if (attributeData == null)
            return null;

        string installerKey = null;
        string fullInstallerName = symbol.ToDisplayString(SymbolDisplayFormat.FullyQualifiedFormat);
        string minimalInstallerName = symbol.ToDisplayString(SymbolDisplayFormat.MinimallyQualifiedFormat);
        string lifetimeScopeName = "ProjectLifetimeScope";
        string installerInstanceType = "PlainCSharp";

        foreach (var kvp in attributeData.NamedArguments)
        {
            switch (kvp.Key)
            {
                case "InstallerKey":
                    installerKey = kvp.Value.Value?.ToString();
                    break;
                case "LifetimeScopeName":
                    lifetimeScopeName = kvp.Value.Value?.ToString();
                    break;
                case "InstallerInstanceType":
                    installerInstanceType = kvp.Value.Value?.ToString();
                    break;
            }
        }

        return new InstallerRegistrationModel(installerKey, fullInstallerName, minimalInstallerName, lifetimeScopeName,
            installerInstanceType);
    }
    
    private static void Execute(SourceProductionContext context, ImmutableArray<InstallerRegistrationModel> models)
    {
        if (models.IsDefaultOrEmpty)
            return;
        
        StringBuilder installerStringBuilder = new();
        IEnumerable<IGrouping<string, InstallerRegistrationModel>> registrationModelGroups =
            models.GroupBy(registrationModel => registrationModel.LifetimeScopeName);
        foreach (IGrouping<string, InstallerRegistrationModel> grouping in registrationModelGroups)
        {
            InstallerManagerStringBuilder.Clear();
            InstallerNamespaceStringBuilder.Clear();
            InstallerTaskStringBuilder.Clear();
        
            InstallerManagerStringBuilder.AppendLine("// Authored by DracoRuan");
            InstallerManagerStringBuilder.AppendLine("// <auto-generated Installer Manager/>");
            InstallerManagerStringBuilder.AppendLine("using VContainer;");
            InstallerManagerStringBuilder.AppendLine("using VContainer.Unity;");
            InstallerManagerStringBuilder.AppendLine("using System.Collections.Generic;");
            InstallerManagerStringBuilder.AppendLine("using Cysharp.Threading.Tasks;");
            InstallerManagerStringBuilder.AppendLine("using UnityEngine.Pool;");
            InstallerManagerStringBuilder.AppendLine("");
            InstallerManagerStringBuilder.AppendLine("namespace DracoRuan.VContainerInstallerSupport.Generated");
            InstallerManagerStringBuilder.AppendLine("{");
            
            foreach (InstallerRegistrationModel model in grouping)
            {
                installerStringBuilder.Clear();
                SourceText generatedCode = model.InstallerInstanceType switch
                {
                    "PlainCSharp" => GetGeneratedSourceForInstallerPlainCSharp(model, installerStringBuilder),
                    "MonoBehaviour" => GetGeneratedSourceForInstallerMonoBehaviour(model, installerStringBuilder),
                    "ScriptableObject" => GetGeneratedSourceForInstallerScriptableObject(model, installerStringBuilder),
                    _ => null
                };

                if (generatedCode == null)
                    continue;

                BuildInstallerManager(model);
                context.AddSource($"VContainer{grouping.Key}{model.MinimalInstallerName}Register.g.cs", generatedCode);
            }

            InstallerManagerStringBuilder.Append("\n");
            InstallerManagerStringBuilder.Append(InstallerNamespaceStringBuilder);
            InstallerManagerStringBuilder.AppendLine($"     public static class {grouping.Key}InstallerRegisterManagerExtension");
            InstallerManagerStringBuilder.AppendLine("     {");
            InstallerManagerStringBuilder.AppendLine($"          public static async UniTask RegisterAll{grouping.Key}Installers(this IContainerBuilder builder)");
            InstallerManagerStringBuilder.AppendLine("          {");
            InstallerManagerStringBuilder.AppendLine("               using (ListPool<UniTask>.Get(out List<UniTask> loadTasks))");
            InstallerManagerStringBuilder.AppendLine("               {");
            InstallerManagerStringBuilder.Append(InstallerTaskStringBuilder);
            InstallerManagerStringBuilder.AppendLine("                    await UniTask.WhenAll(loadTasks);");
            InstallerManagerStringBuilder.AppendLine("               }");
            InstallerManagerStringBuilder.AppendLine("          }");
            InstallerManagerStringBuilder.AppendLine("     }");
            InstallerManagerStringBuilder.AppendLine("}");
        
            SourceText code = SourceText.From(InstallerManagerStringBuilder.ToString(), Encoding.UTF8);
            context.AddSource($"VContainer{grouping.Key}InstallerRegisterManager.g.cs", code);
        }
    }
    
    private static SourceText GetGeneratedSourceForInstallerMonoBehaviour(InstallerRegistrationModel registrationModel, 
        StringBuilder stringBuilder)
    {
        stringBuilder.AppendLine("// Authored by DracoRuan");
        stringBuilder.AppendLine($"// <auto-generated MonoBehaviour Installer {registrationModel.FullInstallerName}/>");
        
        stringBuilder.AppendLine("using VContainer;");
        stringBuilder.AppendLine("using VContainer.Unity;");
        stringBuilder.AppendLine("using UnityEngine;");
        stringBuilder.AppendLine("using Cysharp.Threading.Tasks;");
        stringBuilder.AppendLine("using UnityEngine.AddressableAssets;");
        stringBuilder.AppendLine("using UnityEngine.ResourceManagement.AsyncOperations;");
        stringBuilder.AppendLine("using DracoRuan.Foundation.Initializers.Interfaces;");
        
        stringBuilder.AppendLine("");
        stringBuilder.AppendLine($"namespace DracoRuan.VContainer{registrationModel.LifetimeScopeName}{registrationModel.MinimalInstallerName}Support.Generated");
        stringBuilder.AppendLine("{");
        stringBuilder.AppendLine($"    public static class VContainer{registrationModel.LifetimeScopeName}{registrationModel.MinimalInstallerName}MonoBehaviourInstallerExtensions");
        stringBuilder.AppendLine("    {");
        stringBuilder.AppendLine($"        public static async UniTask RegisterInstallerAuto{registrationModel.LifetimeScopeName}{registrationModel.MinimalInstallerName}(this IContainerBuilder builder)");
        stringBuilder.AppendLine("        {");
        stringBuilder.AppendLine($"              {registrationModel.FullInstallerName} installer = await InitializeInstaller(\"{registrationModel.InstallerKey}\");");
        stringBuilder.AppendLine("              if (installer != null)");
        stringBuilder.AppendLine("              {");
        stringBuilder.AppendLine("                   installer.Install(builder);");
        stringBuilder.AppendLine($"                   installer.transform.SetParent(DracoRuan.Foundation.Initializers.{registrationModel.LifetimeScopeName}.LifetimeScopeInstallerRoot);");
        stringBuilder.AppendLine("                    if (installer is IAsyncInstallable installable)");
        stringBuilder.AppendLine("                         await UniTask.WaitUntil(() => installable.IsInstalled);");
        stringBuilder.AppendLine("              }");
        stringBuilder.AppendLine("        }");
        stringBuilder.AppendLine("");
        
        stringBuilder.AppendLine($"        private static async UniTask<{registrationModel.FullInstallerName}> InitializeInstaller(string installerKey)");
        stringBuilder.AppendLine("        {");
        stringBuilder.AppendLine("              AsyncOperationHandle<GameObject> handle = Addressables.LoadAssetAsync<GameObject>(installerKey);");
        stringBuilder.AppendLine("              await handle;");
        stringBuilder.AppendLine("              if (handle.Status == AsyncOperationStatus.Succeeded)");
        stringBuilder.AppendLine("              {");
        stringBuilder.AppendLine("                  GameObject instance = Object.Instantiate(handle.Result);");
        stringBuilder.AppendLine($"                  if (instance.TryGetComponent(out {registrationModel.FullInstallerName} installer))");
        stringBuilder.AppendLine("                       return installer;");
        stringBuilder.AppendLine("              }");
        stringBuilder.AppendLine("              return null;");
        stringBuilder.AppendLine("        }");
        stringBuilder.AppendLine("    }");
        stringBuilder.AppendLine("}");
        
        return SourceText.From(stringBuilder.ToString(), Encoding.UTF8);
    }
    
